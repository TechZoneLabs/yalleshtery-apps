import '../../domain/entities/user.dart';

class UserModel extends User {
  const UserModel({
    required String userName,
    required String active,
    required String userLevel,
    required String email,
    required String countryCode,
    required String mobile,
    required String avatar,
    required String brief,
    required String clientCode,
    required String address,
    required String authenticationCode,
    required String priceType,
    required String password,
    required String oldPassword,
    required String newPassword,
    required String confirmPassword,
    required String confirm,
    required String wallet,
  }) : super(
          userName: userName,
          active: active,
          userLevel: userLevel,
          email: email,
          countryCode: countryCode,
          mobile: mobile,
          avatar: avatar,
          brief: brief,
          clientCode: clientCode,
          address: address,
          authenticationCode: authenticationCode,
          priceType: priceType,
          password: password,
          oldPassword: oldPassword,
          newPassword: newPassword,
          confirmPassword: confirmPassword,
          confirm: confirm,
          wallet: wallet,
        );

  factory UserModel.fromJson(Map<String, dynamic> map) => UserModel(
        userName: map['user_name'] ?? '',
        active: map['active'] ?? '',
        userLevel: map['user_level'] ?? '',
        email: map['email'] ?? '',
        countryCode: map['country_code'] ?? '',
        mobile: map['mobile'] ?? '',
        avatar: map['avatar'] ?? '',
        brief: map['brief'] ?? '',
        clientCode: map['client_code'] ?? '',
        address: map['address'] ?? '',
        authenticationCode: map['authentication_code'] ?? '',
        priceType: map['price_type'] ?? '',
        password: map['password'] ?? '',
        oldPassword: map['old_password'] ?? '',
        newPassword: map['new_password'] ?? '',
        confirmPassword: map['confirm_password'] ?? '',
        confirm: map['confirm'] ?? '',
        wallet: map['wallet'] ?? '',
      );

  Map<String, dynamic> toJson() => {
        'user_name': userName,
        'active': active,
        'email': email,
        'country_code': countryCode,
        'mobile': mobile,
        'avatar': avatar,
        'brief': brief,
        'client_code': clientCode,
        'address': address,
        'authentication_code': authenticationCode,
        'password': password,
        'user_level': userLevel,
        'price_type': priceType,
        'wallet': wallet,
      };
}
